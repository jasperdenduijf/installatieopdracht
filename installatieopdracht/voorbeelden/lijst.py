#Een string is de naam voor een woord; een aantal opeenvolgende karakters. 
#Een string wordt altijd weergegeven tussen aanhalingstekens.
naam = "Jasper"

print("Mijn naam is " + naam)

#Lijsten zijn verzamelingen van variabelen.
#Lijsten worden opgeslagen tussen rechte haakjes, gescheiden door komma's.
getallen = [0, 1, 2, 3, 4, 5, 6]
boodschappen = ["ei", "appel", "koek", "melk", "brood"]

#Je kunt specifieke elementen selecteren uit een lijst.
#Zet achter de variabele de index in rechte haken. Indices beginnen met 0.
print(boodschappen[1])
print(getallen[3])

#Dit werkt ook voor strings!
print(naam[0])